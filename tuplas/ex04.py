nomes='maria','maria','roberta','joaquim','joao','ana','barbara'

print('quantidade total', len(nomes)) # metodo para exibir a quantidade de informação da tupla

print('em ordem alfabetica', sorted(nomes)) # metodo para exibir as informações em ordem alfabetica 

print('posição', nomes.index('maria')) # mostra a posição que se encontra o dado na tupla

print('repetição de caracteres', nomes.count('maria')) # mostra quantas vezes uma informação se repete